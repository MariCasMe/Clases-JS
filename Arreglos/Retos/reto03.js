//Crear una función con un bucle while que imprima del 1 al n entre corchetes
let n=10;//Declaramos n y le asignamos un valor
let i=1;//Declaramos un auxiliar para la iteración.
while(i<=n){//el ciclo se repite mientras i sea menor o igual a n
    console.log(`[${i}]`); //imprimimos i, que va de 1 hasta n
    i++; //incremento de i 
}